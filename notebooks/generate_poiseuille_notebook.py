    "def run_poiseuille(res_tuple, save_output=False, filename=None):\n",
    "    nx, ny, nz = res_tuple\n",
    "    L = 1.0\n",
    "    slab_thickness = 0.2\n",
    "    H = L - slab_thickness\n",
    "    \n",
    "    sdf_values, spacing = generate_slab_sdf(nx, ny, nz, L, slab_thickness)\n",
    "    dx, dy, dz = spacing\n",
    "    \n",
    "    sdf_data = pnm_backend.SDFData(\n",
    "        sdf_values, \n",
    "        pnm_backend.int3(nx, ny, nz),\n",
    "        pnm_backend.float3(0,0,0),\n",
    "        pnm_backend.float3(dx, dy, dz)\n",
    "    )\n",
    "    solver = pnm_backend.CFDSolver(\n",
    "        pnm_backend.int3(nx, ny, nz), \n",
    "        pnm_backend.float3(dx, dy, dz)\n",
    "    )\n",
    "    solver.initialize(sdf_data)\n",
    "    \n",
    "    rho = 1.0\n",
    "    nu = 0.01\n",
    "    g_x = 1.0e-2\n",
    "    solver.set_body_force(pnm_backend.float3(g_x, 0, 0))\n",
    "    # Time step limited by smallest cell size\n",
    "    dt = 0.1 * min(dx, dy, dz)\n",
    "    \n",
    "    print(f\"Running Grid={nx}x{ny}x{nz}...\")\n",
    "    for i in range(5000):\n",
