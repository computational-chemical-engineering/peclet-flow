    "def generate_slab_sdf(nx, ny, nz, L, slab_thickness):\n",
    "    # Anisotropic resolution\n",
    "    # Domain size L is cubic for now, so L_x=L_y=L_z = L\n",
    "    # If L were a vector, we'd do dx = Lx/nx, etc.\n",
    "    dx = L / nx\n",
    "    dy = L / ny\n",
    "    dz = L / nz\n",
    "    \n",
    "    x = np.linspace(0, L, nx, endpoint=False) + 0.5 * dx\n",
    "    y = np.linspace(0, L, ny, endpoint=False) + 0.5 * dy\n",
    "    z = np.linspace(0, L, nz, endpoint=False) + 0.5 * dz\n",
    "    \n",
    "    X, Y, Z = np.meshgrid(x, y, z, indexing='ij')\n",
    "    \n",
    "    yc = L/2\n",
    "    # Distance to slab surface (walls at yc +/- t/2)\n",
    "    # Positive outside slab (fluid), Negative inside slab (solid)\n",
    "    # Actually pnm_backend assumes SDF < 0 is SOLID.\n",
    "    # So inside slab (y near yc), we want dist < 0.\n",
    "    # dist = |y - yc| - t/2\n",
    "    dist = np.abs(Y - yc) - slab_thickness/2.0\n",
    "    \n",
    "    return dist.ravel(order='F').astype(np.float32), (dx, dy, dz)\n",
    "\n",
    "def get_analytical_profile(y_coords, L, H, g, nu):\n",
    "    # y_coords: global y positions [0, L]\n",
    "    # Fluid channel is wrapped around y=0/L.\n",
    "    # Slab is at [L/2 - t/2, L/2 + t/2].\n",
    "    # Channel center is effectively at y=0 (or y=L).\n",
    "    # We map global y to channel coordinate y' in [-H/2, H/2].\n",
    "    \n",
    "    # If y < L/2: It is the 'bottom' half (connected to y=0). y' = y\n",
    "    # If y > L/2: It is the 'top' half (connected to y=L). y' = y - L\n",
    "    # Note: Walls are at y = (L-H)/2 and y = (L+H)/2 ? No.\n",
    "    # Slab t = L - H. Slab center L/2.\n",
    "    # Walls are at L/2 - t/2 = L/2 - (L-H)/2 = H/2.\n",
    "    # And L/2 + t/2 = L - H/2.\n",
    "    # So fluid is in [0, H/2] and [L - H/2, L].\n",
    "    \n",
    "    u_prof = np.zeros_like(y_coords)\n",
    "    U_max = (g * H**2) / (8.0 * nu)\n",
    "    \n",
    "    for i, y in enumerate(y_coords):\n",
    "        # Transform to centered coordinate y_prime\n",
    "        if y < L/2:\n",
    "            y_prime = y\n",
    "        else:\n",
    "            y_prime = y - L\n",
    "            \n",
    "        # Check if in fluid\n",
    "        if abs(y_prime) <= H/2:\n",
    "            u_prof[i] = U_max * (1.0 - (2.0 * y_prime / H)**2)\n",
    "        else:\n",
    "            u_prof[i] = 0.0 # In solid\n",
    "            \n",
    "    return u_prof\n",
