    "# Verification: Plane Poiseuille Flow\n",
    "\n",
    "This notebook verifies the solver accuracy by simulating gravity-driven flow between parallel plates (Plane Poiseuille Flow).\n",
    "\n",
    "We create a periodic domain with a solid slab in the center. The fluid wraps around the periodic boundaries at $y=0$ and $y=L$, effectively creating a channel of height $H = L - t_{slab}$.\n",
    "\n",
    "## Analytical Solution\n",
    "For a channel of height $H$ driven by body force $g_x$:\n",
    "$$ U_{avg} = \\frac{g H^2}{12 \\nu} $$\n",
    "$$ U_{max} = \\frac{1.5}{U_{avg}} = \\frac{g H^2}{8 \\nu} $$\n",
    "$$ u(y') = U_{max} \\left( 1 - \\left( \\frac{2 y'}{H} \\right)^2 \\right) $$\n",
    "where $y'$ is the distance from the channel center (which is at global $y=0$ in our periodic setup)."
