    "def generate_sc_sdf(phi, res_n, L=1.0):\n",
    "    \"\"\"Generates Signed Distance Field for a Simple Cubic array of spheres.\"\"\"\n",
    "    # Use (Nx, Ny, Nz) shape with Fortran-order (x varies fastest) for consistency with user preference\n",
    "    dx = L/res_n\n",
    "    x = np.linspace(0, L, res_n, endpoint=False) + 0.5 * dx\n",
    "    y = np.linspace(0, L, res_n, endpoint=False) + 0.5 * dx\n",
    "    z = np.linspace(0, L, res_n, endpoint=False) + 0.5 * dx\n",
    "    \n",
    "    # indexing='ij' gives shape (Nx, Ny, Nz)\n",
    "    X, Y, Z = np.meshgrid(x, y, z, indexing='ij')\n",
    "    \n",
    "    R = (phi * 3.0 / (4.0 * np.pi))**(1.0/3.0) * L\n",
    "    \n",
    "    xc, yc, zc = L/2, L/2, L/2\n",
    "    dist = np.sqrt((X - xc)**2 + (Y - yc)**2 + (Z - zc)**2)\n",
    "    sdf = dist - R\n",
    "    \n",
    "    # ravel(order='F') ensures we flatten (x,y,z) as x-fast, y-med, z-slow\n",
    "    # which matches the C++ linear index: idx = z*ny*nx + y*nx + x\n",
    "    return sdf.ravel(order='F').astype(np.float32), R, (dx, dy, dz)\n",
    "\n",
    "def get_analytical_k(phi):\n",
    "    \"\"\"Returns reference Drag Factor K from Sangani & Acrivos (1982) for Simple Cubic.\"\"\"\n",
    "    phis = np.array([0.05, 0.10, 0.20, 0.30, 0.40, 0.50])\n",
    "    ks   = np.array([4.95, 6.10, 9.10, 13.6, 21.6, 39.5])\n",
    "    if phi < phis[0]: return 1.0 # Dilute limit approximation\n",
    "    return np.interp(phi, phis, ks)\n",
    "\n",
    "def save_vti(filename, res_int3, spacing_float3, u, v, w, p):\n",
    "    \"\"\"Saves field data to VTI format.\"\"\"\n",
    "    nx, ny, nz = res_int3.x, res_int3.y, res_int3.z\n",
    "    dx, dy, dz = spacing_float3.x, spacing_float3.y, spacing_float3.z\n",
    "    \n",
    "    # Interpolate staggered velocity to cell centers for correct visualization\n",
    "    # u_stag(i,j,k) is at i-1/2. u_cc(i) = 0.5 * (u(i) + u(i+1))\n",
    "    \n",
    "    # Reshape using Fortran order to get (Nx, Ny, Nz) shape [x, y, z]\n",
    "    # This interprets the linear buffer (X-fast) correctly into [i,j,k]\n",
    "    u_grid = u.reshape((nx, ny, nz), order='F')\n",
    "    v_grid = v.reshape((nx, ny, nz), order='F')\n",
    "    w_grid = w.reshape((nx, ny, nz), order='F')\n",
    "    \n",
    "    # Check periodicity logic: pnm_backend uses periodic wrapping\n",
    "    # With shape [x, y, z]:\n",
    "    # Axis 0 is X\n",
    "    # Axis 1 is Y\n",
    "    # Axis 2 is Z\n",
    "    u_cc = 0.5 * (u_grid + np.roll(u_grid, -1, axis=0))\n",
    "    v_cc = 0.5 * (v_grid + np.roll(v_grid, -1, axis=1))\n",
    "    w_cc = 0.5 * (w_grid + np.roll(w_grid, -1, axis=2))\n",
    "    \n",
    "    # Flatten back to X-fast 1D streams for VTI writing\n",
    "    u_flat = u_cc.ravel(order='F')\n",
    "    v_flat = v_cc.ravel(order='F')\n",
    "    w_flat = w_cc.ravel(order='F')\n",
    "\n",
    "    with open(filename, 'w') as f:\n",
    "        f.write('<?xml version=\"1.0\"?>\\n')\n",
    "        f.write('<VTKFile type=\"ImageData\" version=\"0.1\" byte_order=\"LittleEndian\">\\n')\n",
    "        f.write(f'  <ImageData WholeExtent=\"0 {nx-1} 0 {ny-1} 0 {nz-1}\" Origin=\"0 0 0\" Spacing=\"{dx} {dy} {dz}\">\\n')\n",
    "        f.write(f'    <Piece Extent=\"0 {nx-1} 0 {ny-1} 0 {nz-1}\">\\n')\n",
    "        f.write('      <PointData Scalars=\"Pressure\" Vectors=\"Velocity\">\\n')\n",
    "        \n",
    "        f.write('        <DataArray type=\"Float32\" Name=\"Velocity\" NumberOfComponents=\"3\" format=\"ascii\">\\n')\n",
    "        # Writing loop can be slow for large files in python, but acceptable for validation cases\n",
    "        for i in range(len(u_flat)):\n",
    "            f.write(f\"{u_flat[i]} {v_flat[i]} {w_flat[i]} \")\n",
    "        f.write('\\n        </DataArray>\\n')\n",
    "        \n",
    "        f.write('        <DataArray type=\"Float32\" Name=\"Pressure\" NumberOfComponents=\"1\" format=\"ascii\">\\n')\n",
    "        for val in p:\n",
    "            f.write(f\"{val} \")\n",
    "        f.write('\\n        </DataArray>\\n')\n",
    "        \n",
    "        f.write('      </PointData>\\n')\n",
    "        f.write('    </Piece>\\n')\n",
    "        f.write('  </ImageData>\\n')\n",
    "        f.write('</VTKFile>\\n')\n",
    "    print(f\"Saved {filename}\")"
