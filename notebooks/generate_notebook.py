    "class SimulationRunner:\n",
    "    def __init__(self, phi, res_n, L=1.0):\n",
    "        self.phi = phi\n",
    "        self.res_n = res_n\n",
    "        self.L = L\n",
    "        \n",
    "        self.sdf_values, self.R, self.spacing_tuple = generate_sc_sdf(phi, res_n, L)\n",
    "        self.dx, self.dy, self.dz = self.spacing_tuple\n",
    "        \n",
    "        # Setup Data Objects\n",
    "        self.sdf_data = pnm_backend.SDFData(\n",
    "            self.sdf_values, \n",
    "            pnm_backend.int3(res_n, res_n, res_n),\n",
    "            pnm_backend.float3(0,0,0),\n",
    "            pnm_backend.float3(self.dx, self.dy, self.dz)\n",
    "        )\n",
    "        self.solver = pnm_backend.CFDSolver(\n",
    "            pnm_backend.int3(res_n, res_n, res_n), \n",
    "            pnm_backend.float3(self.dx, self.dy, self.dz)\n",
    "        )\n",
    "        self.solver.initialize(self.sdf_data)\n",
    "        \n",
    "        # Parameters (Low Re)\n",
    "        self.rho = 1e-3\n",
    "        self.mu = 1e0\n",
    "        self.f_mag = 1.0\n",
    "        self.solver.set_body_force(pnm_backend.float3(self.f_mag, 0, 0))\n",
    "        self.solver.set_rho(self.rho)\n",
    "        self.solver.set_mu(self.mu)\n",
    "        \n",
    "        # Solver Parameters for Implicit Scheme\n",
    "        self.solver.set_pressure_solver_params(100, 1e-16)\n",
    "        self.solver.set_velocity_solver_params(200, 1e-16)\n",
    "\n",
    "    def run(self, dt=1.0, max_steps=10000, save_output=False, filename=None):\n",
    "        # Explicit limit is roughly 0.5*dx^2/nu ~ 0.5 * (1/64)^2 / 0.01 ~ 0.01\n",
    "        # We use a much larger step to speed up convergence to steady state.\n",
    "        \n",
    "        u_mean_history = []\n",
    "        \n",
    "        print(f\"Running phi={self.phi:.3f}, Res={self.res_n}^3, dt={dt:.4f}...\")\n",
    "        \n",
    "        start_time = time.time()\n",
    "        for i in range(max_steps):\n",
    "            # Use Implicit Step\n",
    "            self.solver.step(dt)\n",
    "            \n",
    "            if i % 50 == 0:\n",
    "                u_field = np.array(self.solver.get_u())\n",
    "                u_mean = np.mean(u_field)\n",
    "                u_mean_history.append(u_mean)\n",
    "                \n",
    "                if len(u_mean_history) > 5:\n",
    "                    # Relative change convergence check\n",
    "                    if abs(u_mean_history[-1]) > 1e-12:\n",
    "                        err = abs(u_mean_history[-1] - u_mean_history[-2]) / abs(u_mean_history[-1])\n",
    "                        if err < 1e-5:\n",
    "                            print(f\"  Converged at step {i}. Mean Vel = {u_mean:.6e}\")\n",
    "                            break\n",
    "        \n",
    "        elapsed = time.time() - start_time\n",
    "        u_field = np.array(self.solver.get_u())\n",
    "        U_sup = np.mean(u_field)\n",
    "        \n",
    "        # Calculate K\n",
    "        F_drag = self.f_mag * (self.L**3)\n",
    "        K = F_drag* (1-self.phi) / (6.0 * np.pi * self.mu * self.R * U_sup)\n",
    "        \n",
    "        print(f\"  K_sim = {K:.4f} (Time: {elapsed:.2f}s)\")\n",
    "        \n",
    "        if save_output and filename:\n",
    "            v = np.array(self.solver.get_v())\n",
    "            w = np.array(self.solver.get_w())\n",
    "            p = np.array(self.solver.get_p())\n",
    "            save_vti(filename, pnm_backend.int3(self.res_n, self.res_n, self.res_n), pnm_backend.float3(self.dx, self.dy, self.dz), u_field, v, w, p)\n",
    "            \n",
    "        return K, U_sup"
